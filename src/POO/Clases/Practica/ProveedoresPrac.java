
package POO.Clases.Practica;


public class ProveedoresPrac {
    private Integer idProvee, telefono;
    private String descrip, direc;

    public Integer getIdProvee() {
        return idProvee;
    }

    public void setIdProvee(Integer idProvee) {
        this.idProvee = idProvee;
    }

    public Integer getTelefono() {
        return telefono;
    }

    public void setTelefono(Integer telefono) {
        this.telefono = telefono;
    }

    public String getDescrip() {
        return descrip;
    }

    public void setDescrip(String descrip) {
        this.descrip = descrip;
    }

    public String getDirec() {
        return direc;
    }

    public void setDirec(String direc) {
        this.direc = direc;
    }
    
    
    
}
